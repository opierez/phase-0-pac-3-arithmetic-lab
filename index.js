function add(num1, num2) {
    return num1 + num2;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function increment(num) {
    return num += 1;
}

function decrement(num) {
    return num -= 1;
}

function makeInt(num) {
    return parseInt(num, 10);
}

function preserveDecimal(num) {
    return parseFloat(num);
}